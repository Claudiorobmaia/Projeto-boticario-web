/*--card 1 --------*/
const h2Element = document.getElementById('productDescription-1');
const buttonElement = document.getElementById('buyButton-1');
const cardElement = document.getElementById('card-1');




cardElement.addEventListener('mouseover', function() {
    h2Element.classList.add('hidden');
    buttonElement.classList.remove('hidden');
});

cardElement.addEventListener('mouseout', function() {
    buttonElement.classList.add('hidden');
    h2Element.classList.remove('hidden');
  
});


